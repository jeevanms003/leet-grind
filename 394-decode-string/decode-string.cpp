class Solution {
public:
    string decodeString(string s) {
        stack<int> counts;
        stack<string> resultStack;
        string curr = "";
        int num = 0;

        for (char c : s) {
            if (isdigit(c)) {
                num = num * 10 + (c - '0');
            } 
            else if (c == '[') {
                counts.push(num);
                resultStack.push(curr);
                num = 0;
                curr = "";
            } 
            else if (c == ']') {
                int repeat = counts.top(); counts.pop();
                string temp = curr;
                curr = resultStack.top(); resultStack.pop();
                while (repeat--) curr += temp;
            } 
            else {
                curr += c;
            }
        }
        return curr;
    }
};
